package net.yustinus.crud.backend.beans;

import java.util.Date;

import net.yustinus.crud.backend.dto.ProfileDto;

/**
 * @author Yustinus Nugroho
 *
 */
public class UserBean {
	
	private int userId;
	private String username;
	private String nip;
	private String userPassword;
	private ProfileDto userProfile;
	private Date lastLogin;
	private boolean userDisable;
	
	public UserBean() {
		
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getNip() {
		return nip;
	}

	public void setNip(String nip) {
		this.nip = nip;
	}

	public String getUserPassword() {
		return userPassword;
	}

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	public ProfileDto getUserProfile() {
		return userProfile;
	}

	public void setUserProfile(ProfileDto userProfile) {
		this.userProfile = userProfile;
	}

	public Date getLastLogin() {
		return lastLogin;
	}

	public void setLastLogin(Date lastLogin) {
		this.lastLogin = lastLogin;
	}

	public boolean isUserDisable() {
		return userDisable;
	}

	public void setUserDisable(boolean userDisable) {
		this.userDisable = userDisable;
	}

}
